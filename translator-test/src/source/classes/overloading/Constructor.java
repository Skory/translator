package source.classes.overloading;

public class Constructor {

    public Constructor(){ System.out.println("No arg"); }
    public Constructor(byte b){ System.out.println("byte"); }
    public Constructor(char c){ System.out.println("char"); }
    public Constructor(short s){ System.out.println("short"); }
    public Constructor(int i){ System.out.println("int"); }
    public Constructor(long l){ System.out.println("long"); }
    public Constructor(float f){ System.out.println("float"); }
    public Constructor(double d){ System.out.println("double"); }
    public Constructor(String d){ System.out.println("double"); }
    public Constructor(Object d){ System.out.println("double"); }
    
}
